function openMenu() {
    document.getElementById("myNav").style.width = "25%";
    document.getElementById("blocco").style.width = "100%";
    document.getElementById("blocco").style.right = "25%";
  }

  function closeMenu() {
    document.getElementById("myNav").style.width = "0%";
    document.getElementById("blocco").style.width = "0%";
    document.getElementById("blocco").style.right = "0%";
}